#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*buff;

	buff = s1;
	while (*buff != '\0')
		buff++;
	while (*s2 != '\0')
		*buff++ = *s2++;
	*buff = '\0';
	return (s1);
}
