#include "libft.h"

int		ft_findch(char *str, char c)
{
	if (str == NULL)
		return (0);
	if (c == '\0')
		return (1);
	while (*str)
	{
		if (*str == c)
			return (1);
		str++;
	}
	return (0);
}
