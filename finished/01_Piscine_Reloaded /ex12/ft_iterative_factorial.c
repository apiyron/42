int	ft_iterative_factorial(int nb)
{
	int i;
	int f;

	i = 1;
	f = 1;
	if (nb == 0)
	{
		return (1);
	}
	if ((nb >= 1) && (nb <= 12))
	{
		while (i <= nb)
		{
			f = f * i;
			i++;
		}
	}
	else
	{
		return (0);
	}
	return (f);
}
