#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*buff4ik;

	buff4ik = s1;
	while (*buff4ik != '\0')
		buff4ik++;
	while (*s2 != '\0')
		*buff4ik++ = *s2++;
	*buff4ik = '\0';
	return (s1);
}
